SELECT '=== 직원 현황 ===' AS info;
SELECT e.emp_no, e.emp_name, d.dept_name, p.position_name,
       e.status, e.emp_type, e.base_salary
FROM employee e
JOIN department d   ON e.dept_id     = d.dept_id
JOIN job_position p ON e.position_id = p.position_id
ORDER BY e.emp_id;

SELECT '=== 계정 현황 ===' AS info;
SELECT e.emp_name, a.username, a.role, a.is_active,
       a.login_attempts,
       CASE WHEN a.locked_at IS NOT NULL THEN '잠금' ELSE '정상' END AS lock_status
FROM account a
JOIN employee e ON a.emp_id = e.emp_id
ORDER BY a.account_id;

SELECT '=== 3월 급여 현황 ===' AS info;
SELECT e.emp_name, d.dept_name, s.gross_salary,
       s.total_deduction, s.net_salary, s.status
FROM salary s
JOIN employee e ON s.emp_id   = e.emp_id
JOIN department d ON e.dept_id = d.dept_id
WHERE s.salary_year = 2025 AND s.salary_month = 3
ORDER BY s.gross_salary DESC;

SELECT '=== 대기 중 휴가 신청 ===' AS info;
SELECT e.emp_name, l.leave_type, l.start_date, l.end_date,
       l.days, l.status
FROM leave_request l
JOIN employee e ON l.emp_id = e.emp_id
WHERE l.status = '대기'
ORDER BY l.created_at;

SELECT '=== 2024 하반기 평가 결과 ===' AS info;
SELECT e.emp_name, ev.eval_type, ev.total_score, ev.grade, ev.eval_status
FROM evaluation ev
JOIN employee e ON ev.emp_id = e.emp_id
WHERE ev.eval_year = 2024 AND ev.eval_period = '하반기'
ORDER BY ev.total_score DESC;

SELECT '=== 미읽음 알림 현황 ===' AS info;
SELECT e.emp_name, COUNT(*) AS unread_count
FROM notification n
JOIN employee e ON n.emp_id = e.emp_id
WHERE n.is_read = 0
GROUP BY n.emp_id, e.emp_name
ORDER BY unread_count DESC;